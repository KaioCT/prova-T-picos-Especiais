/******************************************************************************
2. Exibir o quadrado dos números de 10 a 30. 
*******************************************************************************/
#include <iostream>
using namespace std;

int main() {
    for(int i = 10; i <= 30; i++) {
        cout << "O quadrado de " << i << " é " << i*i << endl;
    }
    return 0;
}
